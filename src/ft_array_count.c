#include "fdf.h"

int		ft_array_count(char *str)
{
	int		i;
	int		z;
	int		j;
	int		y;

	i = 0;
	z = 1;
	j = 0;
	y = 0;
	while (str[i] != '\0')
	{
		if (str[i] == ' ')
			j++;
		if (str[i] == '-')
			j++;
		if ((str[i] >= '1' && str[i] <= '9') && \
				(str[z] >= '0' && str[z] <= '9'))
			y++;
		i++;
		z++;
	}
	return (i - (j + y));
}
